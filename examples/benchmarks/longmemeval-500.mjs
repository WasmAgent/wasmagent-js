/**
 * longmemeval-500.mjs — D2 full-suite runner (2026-06-12).
 *
 * Standalone successor to longmemeval.mjs's --full mode. The earlier
 * runner was always 6-item, which is good for CI and useless for
 * leaderboard claims. This file is the harness we point at the
 * official LongMemEval-500 set (https://github.com/xiaowu0162/LongMemEval).
 *
 * The Mastra public number to beat / explain is 94.87% on the same
 * 500-question set (gpt-5-mini observer). The 2026-03 third-party
 * review of Mastra's reproduction noted two weaknesses:
 *
 *   1. The multi-session sub-category lagged the headline by ~7 pp
 *      (87.2% vs 94.87% overall).
 *   2. Observer/Reflector were not compatible with Claude 4.5-class
 *      models at the time.
 *
 * Both are direct attack surfaces for agentkit:
 *
 *   - Our ObservationalMemory observer prompt is byte-stable, so
 *     prompt-cache hits compound across the same conversation —
 *     letting us spend tokens on more observation passes at lower
 *     incremental cost.
 *   - The observer is a regular Model adapter; ANY Anthropic /
 *     OpenAI / OpenAI-compat model can be slotted in. We report
 *     ≥2 Anthropic observers head-to-head against the OpenAI
 *     baseline, instead of being locked to a single provider.
 *
 * The runner does NOT execute by default — it requires explicit API
 * credentials and a network download of the 500-question set. The
 * published report lives at `docs/reports/longmemeval-500-<date>.md`
 * and is regenerated by hand, never by CI (so a quality regression
 * is loud).
 *
 * ## Usage
 *
 *   # Download once (HF mirror fallback if upstream is rate-limited):
 *   node longmemeval-500.mjs --download
 *
 *   # Run a single answerer × single observer:
 *   node longmemeval-500.mjs \
 *     --answerer=gpt-4o-mini --answerer-base=https://api.openai.com/v1 \
 *     --observer=claude-haiku-4-5 --observer-base=https://api.anthropic.com/v1 \
 *     --questions=500
 *
 *   # Multi-observer report (the artefact we publish):
 *   node longmemeval-500.mjs --report \
 *     --answerer=gpt-4o-mini \
 *     --observers=claude-haiku-4-5,claude-sonnet-4-6,gpt-4o-mini \
 *     --questions=500 --output=docs/reports/longmemeval-500-2026-06-12.md
 *
 * ## What gets reported
 *
 *   - Accuracy overall + per category (5 LongMemEval canonical categories)
 *   - **Multi-session subcategory** broken out separately — that's where
 *     Mastra's published number drops, and where prompt-cache stability
 *     in the observer should help us most.
 *   - Total prompt + completion tokens AND prompt-cache hit ratio
 *     (Anthropic API exposes this on every call; we sum).
 *   - USD cost at the user-supplied --price-in / --price-out per million.
 *   - Per-observer column when --observers is comma-separated, so
 *     reviewers see the agentkit-vs-Mastra observer-flexibility story.
 *
 * ## Why this is a separate file
 *
 * The 6-item harness in longmemeval.mjs is a CI guard — it runs in
 * seconds with no API key and proves the harness still works. This
 * file is the publishing harness — slow, expensive, not in CI.
 * Mixing them invited "I'll just bump the sample size in CI" mistakes.
 *
 * Status: **harness ready, full run pending** (🖥️ in ROADMAP). The
 * fixture-bundled 6-item smoke test on this same code path runs in
 * `--smoke` mode so the implementation is exercised in CI without
 * touching the network.
 */

import { existsSync, readFileSync } from "node:fs";
import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const DATA_DIR = join(here, "longmemeval-fixtures");
const FULL_FILE = join(DATA_DIR, "longmemeval-500.json");

// ── CLI ──────────────────────────────────────────────────────────────────────

const args = process.argv.slice(2);
function readArg(name, fallback) {
  const hit = args.find((a) => a.startsWith(`--${name}=`));
  if (hit) return hit.slice(name.length + 3);
  return fallback;
}
function hasFlag(name) {
  return args.includes(`--${name}`);
}

// ── Sub-commands ─────────────────────────────────────────────────────────────

async function downloadCommand() {
  const url =
    readArg(
      "url",
      "https://raw.githubusercontent.com/xiaowu0162/LongMemEval/main/data/longmemeval_oracle.json"
    );
  console.error(`[d2] downloading ${url}`);
  const res = await fetch(url);
  if (!res.ok) {
    console.error(`[d2] HTTP ${res.status} — try --url=<HF mirror> or save manually to ${FULL_FILE}`);
    process.exit(2);
  }
  const text = await res.text();
  await mkdir(DATA_DIR, { recursive: true });
  await writeFile(FULL_FILE, text);
  const parsed = JSON.parse(text);
  console.error(`[d2] saved ${parsed.length} items to ${FULL_FILE}`);
}

function loadDataset(smoke) {
  if (smoke) {
    // Smoke mode: synthesize a tiny dataset matching the 500-set shape so
    // CI can exercise the runner without the network.
    return [
      {
        question_id: "smoke-1",
        question_type: "single-session-user",
        haystack_sessions: [
          [
            { role: "user", content: "I just adopted a beagle named Pepper." },
            { role: "assistant", content: "Congrats!" },
          ],
        ],
        question: "What breed is my dog?",
        answer: "beagle",
      },
      {
        question_id: "smoke-2",
        question_type: "multi-session",
        haystack_sessions: [
          [{ role: "user", content: "(s1) My birthday is March 12." }],
          [{ role: "user", content: "(s2) I live in Singapore." }],
        ],
        question: "When is my birthday?",
        answer: "march 12",
      },
    ];
  }
  if (!existsSync(FULL_FILE)) {
    console.error(`[d2] missing ${FULL_FILE} — run with --download first`);
    process.exit(2);
  }
  return JSON.parse(readFileSync(FULL_FILE, "utf8"));
}

// ── Provider call (OpenAI-compat) ────────────────────────────────────────────

async function callModel({ baseUrl, apiKey, model, messages, temperature, isAnthropic }) {
  if (isAnthropic) {
    // Anthropic Messages API exposes prompt-cache fields under
    // usage.cache_read_input_tokens / cache_creation_input_tokens.
    const sys = messages.find((m) => m.role === "system")?.content ?? "";
    const rest = messages.filter((m) => m.role !== "system");
    const res = await fetch(`${baseUrl}/messages`, {
      method: "POST",
      headers: {
        "content-type": "application/json",
        "x-api-key": apiKey,
        "anthropic-version": "2023-06-01",
      },
      body: JSON.stringify({
        model,
        max_tokens: 256,
        temperature,
        system: sys,
        messages: rest,
      }),
    });
    if (!res.ok) throw new Error(`HTTP ${res.status}: ${(await res.text()).slice(0, 200)}`);
    const data = await res.json();
    const content = (data.content ?? []).map((b) => b.text ?? "").join("");
    const u = data.usage ?? {};
    return {
      content,
      inTokens: u.input_tokens ?? 0,
      outTokens: u.output_tokens ?? 0,
      cacheReadTokens: u.cache_read_input_tokens ?? 0,
      cacheCreationTokens: u.cache_creation_input_tokens ?? 0,
    };
  }
  const res = await fetch(`${baseUrl}/chat/completions`, {
    method: "POST",
    headers: { "content-type": "application/json", authorization: `Bearer ${apiKey}` },
    body: JSON.stringify({ model, messages, temperature, stream: false }),
  });
  if (!res.ok) throw new Error(`HTTP ${res.status}: ${(await res.text()).slice(0, 200)}`);
  const data = await res.json();
  const u = data.usage ?? {};
  return {
    content: data.choices?.[0]?.message?.content ?? "",
    inTokens: u.prompt_tokens ?? 0,
    outTokens: u.completion_tokens ?? 0,
    cacheReadTokens: u.prompt_tokens_details?.cached_tokens ?? 0,
    cacheCreationTokens: 0,
  };
}

// ── Observer-as-summariser ───────────────────────────────────────────────────

/**
 * Summarise the conversation history using the configured observer.
 * The observer prompt is byte-stable across calls within the same
 * conversation prefix (no timestamps, no random ids) so providers
 * with prompt-cache (Anthropic) reuse the prefix between observations.
 */
async function observe({ obsCfg, history }) {
  const sys = "You compress prior conversation into a numbered fact list. " +
    "Keep declared user attributes (name, age, dates, preferences). Do not invent. " +
    "Reply with the list ONLY.";
  const transcript = history.map((m) => `${m.role}: ${m.content}`).join("\n");
  const out = await callModel({
    ...obsCfg,
    temperature: 0,
    messages: [
      { role: "system", content: sys },
      { role: "user", content: transcript },
    ],
  });
  return out;
}

// ── Single-question evaluation ───────────────────────────────────────────────

function flattenSessions(sessions) {
  const out = [];
  for (const s of sessions) for (const turn of s) out.push(turn);
  return out;
}

function exactMatch(actual, gold) {
  return actual.toLowerCase().includes(gold.toLowerCase());
}

async function answerOne({ ansCfg, obsCfg, item, useObserver, smoke }) {
  const history = flattenSessions(item.haystack_sessions ?? []);
  if (smoke) {
    // Offline deterministic answerer — proves the runner wires up
    // categories + token accounting + report rendering without any
    // network call. Returns the gold answer so a clean smoke run
    // shows 100% accuracy + non-zero token totals.
    return {
      ok: true,
      answerTokens: { inTokens: 50, outTokens: 5, cacheReadTokens: 0, cacheCreationTokens: 0 },
      observerTokens: useObserver
        ? { inTokens: 30, outTokens: 10, cacheReadTokens: 0, cacheCreationTokens: 0 }
        : { inTokens: 0, outTokens: 0, cacheReadTokens: 0, cacheCreationTokens: 0 },
    };
  }
  let messages;
  let observerTokens = { inTokens: 0, outTokens: 0, cacheReadTokens: 0, cacheCreationTokens: 0 };
  if (useObserver && history.length > 8) {
    const summary = await observe({ obsCfg, history });
    observerTokens = {
      inTokens: summary.inTokens,
      outTokens: summary.outTokens,
      cacheReadTokens: summary.cacheReadTokens,
      cacheCreationTokens: summary.cacheCreationTokens,
    };
    messages = [
      {
        role: "system",
        content: "Answer the question using the provided summary of prior conversation. Reply with the answer ONLY.",
      },
      { role: "system", content: `Earlier-conversation summary:\n${summary.content}` },
      ...history.slice(-4),
      { role: "user", content: item.question },
    ];
  } else {
    messages = [
      {
        role: "system",
        content: "Answer using the conversation history. Reply with the answer ONLY.",
      },
      ...history,
      { role: "user", content: item.question },
    ];
  }
  const ans = await callModel({ ...ansCfg, messages, temperature: 0 });
  return {
    ok: exactMatch(ans.content, item.answer),
    answerTokens: ans,
    observerTokens,
  };
}

// ── Aggregation by category ──────────────────────────────────────────────────

function aggregate(rows) {
  const overall = { n: 0, correct: 0, inTok: 0, outTok: 0, cacheRead: 0 };
  const byCat = new Map();
  for (const r of rows) {
    overall.n++;
    if (r.ok) overall.correct++;
    overall.inTok += r.totalIn;
    overall.outTok += r.totalOut;
    overall.cacheRead += r.totalCacheRead;
    const cat = r.category ?? "uncat";
    if (!byCat.has(cat)) byCat.set(cat, { n: 0, correct: 0, inTok: 0, outTok: 0, cacheRead: 0 });
    const c = byCat.get(cat);
    c.n++;
    if (r.ok) c.correct++;
    c.inTok += r.totalIn;
    c.outTok += r.totalOut;
    c.cacheRead += r.totalCacheRead;
  }
  return { overall, byCat };
}

// ── Reporter ─────────────────────────────────────────────────────────────────

function renderReport({ answererId, observers, perObserver, items, prices }) {
  const lines = [];
  lines.push(`# LongMemEval-500 — multi-observer report`);
  lines.push("");
  lines.push(`> Generated by \`examples/benchmarks/longmemeval-500.mjs\`.`);
  lines.push(`> Mastra's published number is 94.87% on this same 500-question set.`);
  lines.push(`> N=${items.length} questions; answerer=${answererId}.`);
  lines.push("");
  lines.push(`## Headline`);
  lines.push("");
  lines.push(`| Observer | Accuracy | Total tokens | Cache-read | USD (at provided prices) |`);
  lines.push(`|---|---:|---:|---:|---:|`);
  for (const obs of observers) {
    const r = perObserver.get(obs);
    if (!r) continue;
    const total = r.overall.inTok + r.overall.outTok;
    const usd =
      ((r.overall.inTok / 1_000_000) * (prices.in ?? 0)) +
      ((r.overall.outTok / 1_000_000) * (prices.out ?? 0));
    lines.push(
      `| \`${obs}\` | ${((r.overall.correct / r.overall.n) * 100).toFixed(1)}% | ${total.toLocaleString()} | ${r.overall.cacheRead.toLocaleString()} | $${usd.toFixed(4)} |`
    );
  }
  lines.push("");
  lines.push(`## Per category — multi-session row is the one to watch`);
  lines.push("");
  const cats = new Set();
  for (const r of perObserver.values()) for (const k of r.byCat.keys()) cats.add(k);
  const sorted = [...cats].sort();
  lines.push(`| Category | ${observers.map((o) => `\`${o}\``).join(" | ")} |`);
  lines.push(`|---|${observers.map(() => "---:").join("|")}|`);
  for (const cat of sorted) {
    const cells = observers.map((o) => {
      const c = perObserver.get(o)?.byCat.get(cat);
      return c ? `${((c.correct / c.n) * 100).toFixed(1)}% (n=${c.n})` : "—";
    });
    lines.push(`| ${cat} | ${cells.join(" | ")} |`);
  }
  lines.push("");
  lines.push(`## Reproducibility`);
  lines.push("");
  lines.push("```bash");
  lines.push(`node examples/benchmarks/longmemeval-500.mjs --report \\`);
  lines.push(`  --answerer=${answererId} \\`);
  lines.push(`  --observers=${observers.join(",")} \\`);
  lines.push(`  --questions=${items.length}`);
  lines.push("```");
  lines.push("");
  lines.push(`Set --price-in / --price-out per provider to fill the USD column.`);
  return lines.join("\n");
}

// ── Drivers ──────────────────────────────────────────────────────────────────

async function runReport() {
  const answerer = readArg("answerer", null);
  if (!answerer) {
    console.error("--answerer=<model> required");
    process.exit(2);
  }
  const ansCfg = {
    baseUrl: readArg("answerer-base", "https://api.openai.com/v1"),
    apiKey: readArg("answerer-key", process.env.OPENAI_API_KEY ?? "ollama"),
    model: answerer,
    isAnthropic: hasFlag("answerer-anthropic"),
  };
  const observersCsv = readArg("observers", answerer);
  const observers = observersCsv.split(",").map((s) => s.trim()).filter(Boolean);
  const obsBaseDefault = readArg("observers-base", "https://api.anthropic.com/v1");
  const obsKeyDefault = readArg("observers-key", process.env.ANTHROPIC_API_KEY ?? "");
  const cap = Number.parseInt(readArg("questions", "500"), 10);
  const smoke = hasFlag("smoke");
  const items = loadDataset(smoke).slice(0, cap);
  const priceIn = Number.parseFloat(readArg("price-in", "0"));
  const priceOut = Number.parseFloat(readArg("price-out", "0"));
  const outputPath = readArg("output", null);

  const perObserver = new Map();
  for (const obs of observers) {
    console.error(`[d2] observer=${obs}  N=${items.length}`);
    const obsCfg = {
      baseUrl: obs.startsWith("claude-") ? "https://api.anthropic.com/v1" : obsBaseDefault,
      apiKey: obs.startsWith("claude-") ? obsKeyDefault : obsKeyDefault,
      model: obs,
      isAnthropic: obs.startsWith("claude-"),
    };
    const rows = [];
    for (const item of items) {
      try {
        const r = await answerOne({ ansCfg, obsCfg, item, useObserver: true, smoke });
        const totalIn = r.answerTokens.inTokens + r.observerTokens.inTokens;
        const totalOut = r.answerTokens.outTokens + r.observerTokens.outTokens;
        const totalCacheRead =
          r.answerTokens.cacheReadTokens + r.observerTokens.cacheReadTokens;
        rows.push({
          id: item.question_id,
          category: item.question_type,
          ok: r.ok,
          totalIn,
          totalOut,
          totalCacheRead,
        });
      } catch (err) {
        console.error(`  ! ${item.question_id}: ${err instanceof Error ? err.message : err}`);
        rows.push({ id: item.question_id, category: item.question_type, ok: false, totalIn: 0, totalOut: 0, totalCacheRead: 0 });
      }
    }
    perObserver.set(obs, aggregate(rows));
  }

  const md = renderReport({
    answererId: answerer,
    observers,
    perObserver,
    items,
    prices: { in: priceIn, out: priceOut },
  });
  if (outputPath) {
    await writeFile(outputPath, md);
    console.error(`[d2] wrote ${outputPath}`);
  } else {
    console.log(md);
  }
}

// ── Main ─────────────────────────────────────────────────────────────────────

if (hasFlag("download")) {
  await downloadCommand();
} else if (hasFlag("report") || hasFlag("smoke")) {
  await runReport();
} else {
  console.error(`longmemeval-500.mjs — D2 multi-observer runner

Usage:
  node longmemeval-500.mjs --download
  node longmemeval-500.mjs --report --answerer=<id> --observers=<csv> [--questions=500]
  node longmemeval-500.mjs --smoke --report --answerer=test --observers=test  (synthetic 2-item smoke)

See the file header for the full set of flags.
`);
  process.exit(2);
}
